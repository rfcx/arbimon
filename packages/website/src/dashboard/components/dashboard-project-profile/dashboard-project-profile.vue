    <p class="mt-3 italic">
      Create a population baseline for bird and anuran species distributions across Puerto Rico
    </p>
    <p class="mt-3">
      Puerto Rico has a diverse bird and anuran community, including many endemic, endangered and data deficient species. While species list sexist for these groups and the distributions of a few species have been studied, state and federal agencies do not have species distribution maps for most species and the population status of many species are unknown. These baseline maps are urgently needed given the rapidly changing environment in Puerto Rico, with urban expansion in some areas, forest recovery in others, and climate change affecting the whole island.
    </p>
